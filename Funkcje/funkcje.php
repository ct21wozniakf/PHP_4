trim() - usuwa białe znaki i inne zdefiniowane znaki po obu stronach ciągu.
stripslashes() - usuwa ukośniki odwrotne dodane przez funkcję addslashes().
htmlspecialchars() - konwertuje niektóre wstępnie zdefiniowane znaki na encje HTML.
wstępnie zdefiniowane znaki to:
& &amp;
" &quot;
' &#039;
< &lt;
> &gt; 